/** Resolve a copy key to the single site voice (the `product` variant). */
export function t(key: string): string {
  const v = copy[key];
  if (!v) {
    if (import.meta.env?.DEV) console.warn(`[copy] missing key: ${key}`);
    return '';
  }
  return v[DEFAULT_TONE];
}
